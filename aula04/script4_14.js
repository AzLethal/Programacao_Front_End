let a,b,c,d,e;
a = 50;
b = 120;
c = 200;
d=(a<=b) ? "Verdadeiro" : "Falso";//Comparação ternário, se 'a' <= 'b' então "Verdadeiro", se não "Falso" --> Resultado: "Verdadeiro"
e=(a>=c) ? "Verdadeiro" : "Falso";//Comparação ternário, se 'a' >= 'c' então "Verdadeiro", se não "Falso" --> Resultado: "Falso"
document.write(`d = ${d}<br>`);//d = Verdadeiro
document.write(`e = ${e}<br>`);//e = Falso